void add(a,b){
  print('Addition: ${a+b}');
}

void sub(a,b){
  print('Subtraction: ${a-b}');
}

void mul(a,b){
  print('Multiplication: ${a*b}');
}

void div(a,b){
  print('Division: ${a/b}');
}

void main(){
  int a=8, b=4;
  add(a, b);
  sub(a, b);
  mul(a, b);
  div(a, b);
}